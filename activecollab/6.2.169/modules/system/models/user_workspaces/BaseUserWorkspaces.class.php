/**
 * BaseUserWorkspaces class.
 *
 * @package ActiveCollab.modules.system
 * @subpackage models
 */
abstract class BaseUserWorkspaces extends DataManager
{
    /**
     * Return name of this model.
     *
     * @param  bool   $underscore
     * @return string
     */
    public static function getModelName($underscore = false)
    {
        return $underscore ? 'user_workspaces' : 'UserWorkspaces';
    }

    /**
     * Return name of the table where system will persist model instances.
     *
     * @return string
     */
    public static function getTableName()
    {
        return 'user_workspaces';
    }

    /**
     * All table fields.
     *
     * @var array
     */
    private static $fields = ['id', 'user_id', 'shepherd_account_id', 'shepherd_account_type', 'shepherd_account_url', 'name', 'is_shown_in_launcher', 'is_owner', 'position', 'updated_on'];

    /**
     * Return a list of model fields.
     *
     * @return array
     */
    public static function getFields()
    {
        return self::$fields;
    }

    /**
     * Return class name of a single instance.
     *
     * @return string
     */
    public static function getInstanceClassName()
    {
        return 'UserWorkspace';
    }

    /**
     * Return whether instance class name should be loaded from a field, or based on table name.
     *
     * @return string
     */
    public static function getInstanceClassNameFrom()
    {
        return DataManager::CLASS_NAME_FROM_TABLE;
    }

    /**
     * Return name of the field from which we will read instance class.
     *
     * @return string
     */
    public static function getInstanceClassNameFromField()
    {
        return '';
    }

    /**
     * Return name of this model.
     *
     * @return string
     */
    public static function getDefaultOrderBy()
    {
        return 'position, id';
    }
}
