    public function isValidStateForCountry(
        string $country_code,
        string $state_code
    ): bool
    {
        $upper_country = strtoupper($country_code);
        $upper_state = strtoupper($state_code);

        if ($upper_country === 'AU' && !$this->isValidAuStateCode($upper_state)) {
            return false;
        } elseif ($upper_country === 'US' && !$this->isValidUsStateCode($upper_state)) {
            return false;
        } elseif ($upper_country === 'CA' && !$this->isValidCaStateCode($upper_state)) {
            return false;
        }

        return true;
    }
