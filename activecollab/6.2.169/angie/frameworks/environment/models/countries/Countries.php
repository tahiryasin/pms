    /**
     * Get countries.
     *
     * @return array
     */
    public function getCountries(): array
    {
        return $this->countries;
    }

    /**
     * Get full country name by country code.
     *
     * @param  string $country_code
     * @return string
     */
    public function getCountryName(string $country_code): string
    {
        $country_code = strtoupper($country_code);

        if (isset($this->countries[$country_code])) {
            return $this->countries[$country_code];
        }

        throw new InvalidArgumentException("Country {$country_code} not found.");
    }

    /**
     * Get country code by full country name.
     *
     * @param  string $country_name
     * @return string
     */
    public function getCountryCode(string $country_name): string
    {
        if ($key = array_search($country_name, $this->countries)) {
            return $key;
        }

        throw new InvalidArgumentException("Country {$country_name} not found.");
    }

    public function getStateName(string $state_code): string
    {
        $state_code_upper = strtoupper($state_code);

        if ($this->isValidUsStateCode($state_code_upper)) {
            return $this->us_states[$state_code_upper];
        }
        if ($this->isValidCaStateCode($state_code_upper)) {
            return $this->ca_states[$state_code_upper];
        }
        if ($this->isValidAuStateCode($state_code_upper)) {
            return $this->au_states[$state_code_upper];
        }

        return $state_code;
    }

    public function isValidCountryCode(string $country_code): bool
    {
        return $country_code && array_key_exists(strtoupper($country_code), $this->countries);
    }

